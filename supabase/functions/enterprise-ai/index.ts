import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are CortaneX AI, an intelligent enterprise assistant with access to all business data across multiple portals:

1. **CRM Portal**: Client data, sales pipeline, support tickets, debit collection, quotes, invoices
2. **Management Portal**: Employee data, departments, KPIs, strategic goals, OKRs, workflows
3. **Accounting Portal**: General ledger, AR/AP, banking, payroll, budgets, financial reports
4. **Logistics Portal**: Shipments, carriers, equipment, driver expenses, settlements
5. **ChatFlow Portal**: Chatbot management, conversations, analytics
6. **Customer Portal**: Customer-facing chatbot interactions

Your capabilities:
- Analyze trends and patterns across all business data
- Provide actionable insights and recommendations
- Help with decision-making by weighing options
- Answer questions about any portal's data
- Generate reports and summaries
- Identify risks and opportunities

Context about the current data:
${JSON.stringify(context || {}, null, 2)}

Guidelines:
- Be concise but thorough
- Use specific numbers and data when available
- Prioritize actionable insights
- Flag any concerns or risks
- Suggest next steps when appropriate`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required. Please add credits to your workspace." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Enterprise AI error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
